# str
string = "string type"
print(string)
print(type(string))
# int
i = 2
print(i)
print(type(i))
# float
f = 2.5
print(f)
print(type(f))
# complex
c = 1j
print(c)
print(type(c))
# list
l = ["apple", "banana", "cherry"]
print(l)
print(type(l))
# tuple
t = ("apple", "banana", "cherry")
print(t)
print(type(t))
# range
r = range(5)
print(r)
print(type(r))
# dict
d = {"name": "John", "age": 36, "Sex": "Male"}
print(d)
print(type(d))
# set
s = {"apple", "banana", "cherry"}
print(s)
print(type(s))
# frozenset
fs = frozenset({"apple", "banana", "cherry"})
print(fs)
print(type(fs))
# bool
bnT = True
bnF = False
print(bnT)
print(bnF)
print(type(bnT))
# bytes
bt = b"Hello"
print(bt)
print(type(bt))
# byteArray
btA = bytearray(5)
print(btA)
print(type(btA))
# memoryView
mv = memoryview(bytes(5))
print(mv)
print(type(mv))
